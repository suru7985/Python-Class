def addition(a,b):
    add=a+b
    return add
def subtraction(q,p):
    diff=q-p
    return diff 
def division(x,y):
    div=x/y
    return div 
def multipilcation(u,v):
    mul=u**v
    return mul

if __name__ =="__main__":
    print(addition(5,5))
    print(subtraction(5,5))
    print(division(5,5))
    print(multipilcation(5,5))
